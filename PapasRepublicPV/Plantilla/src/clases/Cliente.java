package clases;

public class Cliente {
	
	// Private attributes
	private int codigo;
	private long numeroDeDocumento;
	private String nombre, tipoDeDocumento;

	// Constructor
	public Cliente(int codigo, String nombre, long numeroDeDocumento, String tipoDeDocumento) {
		this.codigo = codigo;
		this.nombre = nombre;
		this.numeroDeDocumento = numeroDeDocumento;
		this.tipoDeDocumento = tipoDeDocumento;
	}

	// Setters and getters
	public void setCodigo(int codigo) {
		this.codigo = codigo;
	}
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
	public void setNumeroDeDocumento(long numeroDeDocumento) {
		this.numeroDeDocumento = numeroDeDocumento;
	}
	public void setTipoDeDocumento(String tipoDeDocumento) {
		this.tipoDeDocumento = tipoDeDocumento;
	}
	public int getCodigo() {
		return codigo;
	}
	public String getNombre() {
		return nombre;
	}
	public String getTipoDeDocumento() {
		return tipoDeDocumento;
	}
	public long getNumeroDeDocumento() {
		return numeroDeDocumento;
	}
}
